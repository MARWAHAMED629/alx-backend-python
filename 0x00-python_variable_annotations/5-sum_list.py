#!/usr/bin/env python3
"""
This module contains a function for summing a list of floating-point numbers.
"""
from typing import List


def sum_list(input_list: List[float]) -> float:
    """
    Sums a list of floating-point numbers.

    Args:
        input_list (List[float]):The list of floating-point

    Returns:
        float: The sum of the given list of floating-point numbers.
    """
    return sum(input_list)
