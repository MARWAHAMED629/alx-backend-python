#!/usr/bin/env python3
"""
This module contains a simple function for adding two floating-point numbers.
"""


def add(m: float, x: float) -> float:
    """
    Adds two floating-point numbers together.

    Args:
        m (float): The first number.
        x (float): The second number.

    Returns:
        float: The sum of the two numbers.
    """
    return m + x
