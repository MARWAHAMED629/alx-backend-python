#!/usr/bin/env python3
"""
This module contains a simple function for concatenating two strings.
"""


def concat(st1: str, st2: str) -> str:
    """
    Concatenates two strings.

    Args:
        st1 (str): The first string.
        st2 (str): The second string.

    Returns:
        str: The concatenation of the two strings.
    """
    return st1 + st2
